import turtle
"""
@a rectangle
"""
turtle.penup()
turtle.setpos(-200, 200)
turtle.pendown()
turtle.forward(100)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(50)

"""
@a square
"""
turtle.penup()
turtle.setpos(-100, 100)
turtle.pendown()

turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)

"""
@a circle
"""
turtle.penup()
turtle.setpos(-50,-50)
turtle.pendown()
turtle.circle(50)

turtle.done()


